
package com.careconnectapi.api.model;

import java.time.LocalDate;
import java.time.LocalDateTime;

public class  PaymentRequestBody {

    private int billingId;            // Cannot be NULL in the database
    private int appointmentId;        // Cannot be NULL
    private int patientId;            // Cannot be NULL
    private int doctorId;             // Cannot be NULL
    private double totalAmount;       // Cannot be NULL
    private double paidAmount;        // Cannot be NULL
    private Double balanceAmount;     // Can be NULL in the database
    private String paymentStatus;     // Enum: 'Paid', 'Unpaid', 'Partial' (Cannot be NULL)
    private LocalDate paymentDate;    // Can be NULL
    private LocalDateTime createdAt;  // CURRENT_TIMESTAMP
    private LocalDateTime updatedAt;  // CURRENT_TIMESTAMP

    // Getters and setters
    public int getBillingId() {
        return billingId;
    }

    public void setBillingId(int billingId) {
        this.billingId = billingId;
    }

    public int getAppointmentId() {
        return appointmentId;
    }

    public void setAppointmentId(int appointmentId) {
        this.appointmentId = appointmentId;
    }

    public int getPatientId() {
        return patientId;
    }

    public void setPatientId(int patientId) {
        this.patientId = patientId;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(double totalAmount) {
        this.totalAmount = totalAmount;
    }

    public double getPaidAmount() {
        return paidAmount;
    }

    public void setPaidAmount(double paidAmount) {
        this.paidAmount = paidAmount;
    }

    public Double getBalanceAmount() {
        return balanceAmount;
    }

    public void setBalanceAmount(Double balanceAmount) {
        this.balanceAmount = balanceAmount;
    }

    public String getPaymentStatus() {
        return paymentStatus;
    }

    public void setPaymentStatus(String paymentStatus) {
        this.paymentStatus = paymentStatus;
    }

    public LocalDate getPaymentDate() {
        return paymentDate;
    }

    public void setPaymentDate(LocalDate paymentDate) {
        this.paymentDate = paymentDate;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }
}
